                состав брусчатки белый кварцевый песок, придающий материалу светлый оттенок. Для того, чтобы плитка
                приобрела повышенную прочность и высокую степень износостойкости, в ее состав добавляют пластификатор.
                Данный компонент также создает защиту от образования трещин на полученном изделии.</p>

            <p>Если плитка изготавливается самостоятельно, придать ей цвет также можно способом нанесения красителя
